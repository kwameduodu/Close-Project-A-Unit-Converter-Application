package lab.Datatype;

public class MathematicOperators {

	public static void main(String[] args) {
		
		//define variables
		int x = 5;
		int y = 3;
		int z;
		
		// addition
		z = x + y;
		System.out.println(z);
		
		//subtraction
		System.out.println(x-y);
		
		//division
		System.out.println(x/y);
		
		//modulus
		System.out.println(x%y);
		
	  // float vs int
	 
		
		float f = 34.22f;
		int i = 22;
		int result = (int) f + i ;
		
		System.out.println(result);
				
	}
}
